<div class="pitch" markdown="1">

All of the code that powers uwdata is [available on GitHub](http://github.com/jverkoey/uwdata.ca)
under Apache 2.0.

That means you can use it for whatever you like.
------------------------------------------------

It also means that it's simple to fix any bugs on the site or the API.  
All you have to do is

* fork the project,
* push your change,
* and make a pull request on GitHub.

And then I pull your changes into production.
---------------------------------------------

If you have no idea how to use git or haven't heard of GitHub, I suggest you [learn](http://github.com).

uwdata is built off the lovely PHP framework known as [Kohana](http://kohanaphp.com). If you're
looking to rapidly prototype a website in PHP, Kohana should be one of your first choices.

